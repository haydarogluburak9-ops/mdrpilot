// Export localization (mode B): each export is generated in ONE selected
// language and produces a separate file.

import {
  TRANSLATOR_LOCALE_LABELS,
  TRANSLATOR_LOCALES,
  type TranslatorLocale,
} from "@/lib/document-translator/locales";
import type { Lang } from "@/lib/i18n/locales";

export type ExportLanguage = TranslatorLocale;

/** The base/native language. Its revision keeps the original number (no suffix). */
export const PRIMARY_LANGUAGE: ExportLanguage = "tr";

export const EXPORT_LANGUAGES: { value: ExportLanguage; label: string }[] = TRANSLATOR_LOCALES.map(
  (value) => ({ value, label: TRANSLATOR_LOCALE_LABELS[value] }),
);

export function isExportLanguage(v: unknown): v is ExportLanguage {
  return typeof v === "string" && (TRANSLATOR_LOCALES as readonly string[]).includes(v);
}

export function coerceLanguage(v: unknown): ExportLanguage {
  return isExportLanguage(v) ? v : PRIMARY_LANGUAGE;
}

/** Map export language to UI dictionary language. */
export function exportLangToUiLang(lang: ExportLanguage): Lang {
  return lang;
}

const REVISION_SUFFIX: Record<ExportLanguage, string> = {
  "tr": "",
  "en": "e",
  "de": "d",
  "fr": "f",
  "es": "s",
  "it": "i",
  "nl": "n",
};

/** Append the language revision suffix to a version string, e.g. ("v1.0","en") → "v1.0e". */
export function revisionForLang(version: string, lang: ExportLanguage): string {
  const suffix = REVISION_SUFFIX[lang] ?? "";
  return suffix ? `${version}${suffix}` : version;
}

const UPPER_LOCALE: Record<ExportLanguage, string> = {
  "tr": "tr-TR",
  "en": "en-GB",
  "de": "de-DE",
  "fr": "fr-FR",
  "es": "es-ES",
  "it": "it-IT",
  "nl": "nl-NL",
};

/** Locale-aware uppercase (Turkish: i→İ, ı→I; not ASCII toUpperCase). */
export function localeUppercase(text: string, lang: ExportLanguage): string {
  const locale = UPPER_LOCALE[lang];
  try {
    return text.toLocaleUpperCase(locale);
  } catch {
    return text.toUpperCase();
  }
}

/** Short tag used in export file names so each language is a distinct file. */
export function langFileTag(lang: ExportLanguage): string {
  return lang;
}

