export function riskScoreCellClass(score: number): string {
  if (score >= 15) return "bg-red-200/80 text-red-950 dark:bg-red-950 dark:text-red-100";
  if (score >= 9) return "bg-orange-200/80 text-orange-950 dark:bg-orange-950 dark:text-orange-100";
  if (score >= 4) return "bg-yellow-200/80 text-yellow-950 dark:bg-yellow-950 dark:text-yellow-100";
  return "bg-emerald-100/80 text-emerald-950 dark:bg-emerald-950 dark:text-emerald-100";
}
