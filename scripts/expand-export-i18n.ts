function inject(lang: string, map: Record<string, string>) {
  const start = `const ${lang} = mergeExportDict({`;
  const i = s.indexOf(start);
  if (i < 0) throw new Error(`missing ${lang}`);
  const j = s.indexOf("});", i);
  const block = s.slice(i, j);
  let extra = "";
  for (const [k, v] of Object.entries(map)) {
    if (!block.includes(`"${k}"`)) {
      extra += `\n  "${k}": ${JSON.stringify(v)},`;
    }
  }
  if (!extra) return;
  s = s.slice(0, j) + extra + "\n" + s.slice(j);
}

